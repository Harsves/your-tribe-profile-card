$(document).ready(function(){  


$("#namebutton").click(function(){
  $(".name") .hide();

});

$("#namebutton").click(function(){
  $(".degree") .show();

});

$("#degreebutton").click(function(){
  $(".degree") .hide();

});

$("#degreebutton").click(function(){
  $(".photo") .show();

});

$("#photobutton").click(function(){
  $(".photo") .hide();

});

$("#photobutton").click(function(){
  $(".about") .show();

});

$("#aboutbutton").click(function(){
  $(".about") .hide();

});

$("#aboutbutton").click(function(){
  $(".socials") .show();

});

$("#socialsbutton").click(function(){
  $(".socials") .hide();

});

$("#socialsbutton").click(function(){
  $(".name") .show();

});

});